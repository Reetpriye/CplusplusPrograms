/*
C++ was designed with the main intention
of adding object-oriented features to C
language.
As the size of the program increases,
readibility, maintainability and  bug-
free nature of program decreases.
Also, as data was almost neglected, data
security was easily compromised.
Using classes solves this problem by
modelling program as a real world 
scenario.

====Procedure Oriented Programming====
==================POP=================
Consists of writing a set of instructions
for the computer to flow.
Main focus is on functions and not data
flow.
Functions can either use local or global 
value.
Data moves openly from one function to
another function.

=====Object Oriented Programming======
==================OOP=================
Works on the concept of classes and 
objects.
A class is a template to create objects.
Treats data as a critical element.
Decomposes the problem in objects and
builds data and functions around the
object.

====Terminology====
Classes: Basic template for creating
objects.
Objects: Basic run time entities.
Data Abstraction & Encapsulation:Wrapping
data and functions into single unit.
Inheritance: Properties of one class can
be inherited into others.
Polymorphism: Ability to take more than
one forms. e.g. function overloading.
Dynamic Building: Code which will execute
is not known until the program runs.
Message Passing: Object.message
(Information) call format.
*/